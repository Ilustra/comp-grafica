export class Janela {

     public xMin:number;
     public xMax: number;
     public yMin: number;
     public yMax: number;

    constructor(newXmin: number, newYmin: number, newXmax: number, newYmax: number){
        this.xMin = newXmin;
        this.xMax = newXmax;
        this.yMin = newYmin;
        this.yMax = newYmax;
    }
}